class Calculration:
	def add(self, num1, num2):
		result = num1 + num2
		print ("%s + %s = %s" %(num1,num2,result))
	def mul(self, num1, num2):
		result = num1 * num2
		print ("%s * %s = %s" %(num1,num2,result))
	def min(self, num1, num2):
		result = num1 - num2
		print ("%s - %s = %s" %(num1,num2,result))
	def div(self, num1, num2):
		result = num1 / num2
		print ("%s / %s = %s" %(num1,num2,result))
		